// 산술 연산자 
// 산술등호연산 (복합대입연산자)

let x = 10;
x += 5;
x *= 2;

console.log(x);

let y = 10;
y -= 5;
y /= 5;
console.log(y);

console.log(15 % 4);  // 3 나머지 연산자 
console.log(3 ** 3);  // 27 거듭제곱
console.log(+10);
console.log(-10);

let value = 10;
++value; // value = value + 1
value--; // value = value -1
console.log(value);